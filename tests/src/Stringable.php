declare(strict_types=1);

namespace SimpleComplex\Tests\Database;

/**
 * Class having __toString() method.
 *
 * @package SimpleComplex\Tests\Database
 */
class Stringable
{
    protected $value;

    /**
     * @param string $value
     */
    public function __construct(string $value)
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function __toString() : string
    {
        return $this->value;
    }
}
